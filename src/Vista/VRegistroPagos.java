    private void btGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGuardarActionPerformed
        
        if( tipo.equalsIgnoreCase("inscripcion") || tipo.equalsIgnoreCase("mensualidad") ) {
            control.guardarRecibo(tipo,etNumeroDelRecibo.getText(), etMatriculaEnRecibo.getText(),
                etNombreEnRecibo.getText(), etPagadoEnRecibo.getText(), etAdeudoEnRecibo.getText(),
                etMultaEnRecibo.getText(), txAbono.getText());
        } else {
            String curso = (String) cbCursoEsporadico.getSelectedItem();
            if( !curso.equalsIgnoreCase("Seleccione un curso") ) {
                tipo = (String) cbCursoEsporadico.getSelectedItem();
                control.guardarRecibo(tipo,etNumeroDelRecibo.getText(), etMatriculaEnRecibo.getText(),
                etNombreEnRecibo.getText(), etPagadoEnRecibo.getText(), etAdeudoEnRecibo.getText(),
                etMultaEnRecibo.getText(), txAbono.getText());
            }
        }
        
        if( tipo.equalsIgnoreCase("inscripcion") ) {
            control.busquedaConMatricula(etMatriculaEnRecibo.getText(), tipo);
        } else {
            if( tipo.equalsIgnoreCase("mensualidad") ) {
                control.busquedaConMatriculaMensualidad(etMatriculaEnRecibo.getText(), tipo);
            } else {
                String curso = (String) cbCursoEsporadico.getSelectedItem();
                if( !curso.equalsIgnoreCase("Seleccione un curso") ) {
                    int matricula = Integer.parseInt( txMatriculaNombre.getText() );
                    try {
                        control.obtenerCostosCurso( curso, matricula);
                    } catch (Exception ex) {
                        Logger.getLogger(VRegistroPagos.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
            
        }
        //
        txAbono.setText("");
    }//GEN-LAST:event_btGuardarActionPerformed

    private void btBuscarAlumnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarAlumnoActionPerformed
        jpRecibo.setVisible(false);
        if(rbMatricula.isSelected()){
             String matricula = txMatriculaNombre.getText();
            try {
                if(tipo.equalsIgnoreCase( "Inscripcion" ) ){
                    control.busquedaConMatricula(matricula, tipo);
                }
                else{
                    if(tipo.equalsIgnoreCase( "esporadico" ) ){
                        control.busquedaEsporadicoPorMatricula( matricula, cbCursoEsporadico );
                    } else {
                        control.busquedaConMatriculaMensualidad( matricula, tipo );
                    }
                    
                }
            } catch (Exception ex) {
                ex.printStackTrace();
                Logger.getLogger(VRegistroPagos.class.getName()).log(Level.SEVERE, null, ex);

            }
        }else {
            String nombre=txMatriculaNombre.getText();
            String apellidoPaterno=txApellidoPaterno.getText();
            String apellidoMaterno=txApellidoMaterno.getText();
            
            if(rbMensualidad.isSelected()){
            control.busquedaConNombreCompleto(nombre, apellidoPaterno, apellidoMaterno,tipo);
            }else{
            control.busquedaEsporadicoPorNombre(nombre, apellidoPaterno, apellidoMaterno, cbCursoEsporadico);
            }
                 
        }
    }//GEN-LAST:event_btBuscarAlumnoActionPerformed

