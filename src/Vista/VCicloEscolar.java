    public void dcCrearInicioPropert(PropertyChangeEvent pe) {

        if (pe.getPropertyName().compareTo("date") == 0) {

            try {
                controlVista.validarFechaInicioCrear(dcCrearInicio.getDate(),dcCrearFinal.getDate());
            } catch (ParseException ex) {
                Logger.getLogger(VCicloEscolar.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }

    public void dcCrearFinPropert(PropertyChangeEvent pe) {
        if (pe.getPropertyName().compareTo("date") == 0) {

            controlVista.validarFechaFin(dcCrearInicio.getDate(), dcCrearFinal.getDate());

        }
    }

