    @Override
    public void enviarCicloEscolar(Date fechaInicio, Date fechaFin, String nombre) {
        lbNombreCiclo.setText(nombre);
        dcEditarInicio.setDate(fechaInicio);
        dcEditarFinal.setDate(fechaFin);
    }

    @Override
    public void enviarFechaInicio(Date fechaInicio, boolean valida) {

        dcEditarInicio.setDate(fechaInicio);
        dcEditarInicio.setEnabled(valida);
    }

    @Override
    public void enviarFechaFin(boolean valida) {
        if (!valida) {
               ((JTextField) dcEditarFinal.getDateEditor().getUiComponent()).setText("");
        }
        btGuardarCambios.setEnabled(valida);
    }

    @Override
    public void enviarFechaFin(Date fechaFin, boolean valida) {
        dcEditarFinal.setDate(fechaFin);
    }

