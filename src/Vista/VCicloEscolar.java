    private void btCrearCicloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCrearCicloActionPerformed
        try {
            // La variable result permite indicar como terminó la creación
            // del ciclo escolar
            controlVista.crearCiclo(dcCrearInicio.getDate(), dcCrearFinal.getDate());

        } catch (Exception ex) {
            // En caso de ya exista un ciclo actualmente, no se podrá crear
            // ya que comparten el mismo nombre. Para eso se necesita editar el
            // ciclo actual.
            Logger.getLogger(VCicloEscolar.class.getName()).log(Level.SEVERE, null, ex);
            javax.swing.JOptionPane.showMessageDialog(this,
                "Ya se encuentra un ciclo escolar en progreso."
                + "\nPara editar éste ciclo, seleccione "
                + "\"Modificar ciclo escolar\"", "Error de Ciclo",
                javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btCrearCicloActionPerformed
