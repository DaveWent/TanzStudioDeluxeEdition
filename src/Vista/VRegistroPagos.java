    public void BusquedaPorMatricula(String[] alumnoMatricula, ArrayList<String> cuenta) {
        String nombreCompleto = alumnoMatricula[1] +" "+ alumnoMatricula[2] +" "+ alumnoMatricula[3];
        etNombreEnRecibo.setText(nombreCompleto);
        etMatriculaEnRecibo.setText(alumnoMatricula[0]);
        etNumeroDelRecibo.setText(cuenta.get(0));
        etPagadoEnRecibo.setText(cuenta.get(1));
        double costo = Double.parseDouble(cuenta.get(2));
        double saldo = Double.parseDouble(cuenta.get(1));
        double adeudo = costo - saldo;
        etAdeudoEnRecibo.setText(Double.toString(adeudo));
        etMultaEnRecibo.setText(cuenta.get(3));
        jpRecibo.setVisible(true);
    }

    public void actualizaDatosAlumnoEnEsporadico(String[] datos, JComboBox cbCursos ) {
        String nombreCompleto = datos[ 1 ] + " "+ datos[ 2 ] +" "+ datos[ 3 ];
        etNombreEnRecibo.setText(nombreCompleto);
        etMatriculaEnRecibo.setText(datos[ 0 ]);
        cbCursoEsporadico = cbCursos;
        jpRecibo.setVisible(true);
    }

    public void actualizarCostos(ArrayList<String> cuenta) {
        etNumeroDelRecibo.setText(cuenta.get(0));
        etPagadoEnRecibo.setText(cuenta.get(1));
        double costo = Double.parseDouble(cuenta.get(2));
        double saldo = Double.parseDouble(cuenta.get(1));
        double adeudo = costo - saldo;
        etAdeudoEnRecibo.setText(Double.toString(adeudo));
        etMultaEnRecibo.setText(cuenta.get(3));
        jpRecibo.setVisible(true);
    }

