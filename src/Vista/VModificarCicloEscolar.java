    public void initCompenents2() {
        //btCrearCiclo.setEnabled(false);
        // Transformar fecha a Español
        dcEditarInicio.setLocale(new java.util.Locale("es", "ES"));
        // Transformar fecha a formato día/mes/año
        dcEditarInicio.setDateFormatString("d/MMM/yyyy");
        // El proceso es repetido para cada JDateChooser
        dcEditarFinal.setLocale(new java.util.Locale("es", "ES"));
        dcEditarFinal.setDateFormatString("d/MMM/yyyy");
        dcEditarInicio.setLocale(new java.util.Locale("es", "ES"));
        dcEditarInicio.setDateFormatString("d/MMM/yyyy");
        dcEditarFinal.setLocale(new java.util.Locale("es", "ES"));
        dcEditarFinal.setDateFormatString("d/MMM/yyyy");

        dcEditarInicio.getDateEditor().addPropertyChangeListener(new PropertyChangeListener() {
            public void propertyChange(PropertyChangeEvent e) {
                dcModifInicioPropert(e);
            }
        });
        dcEditarFinal.getDateEditor().addPropertyChangeListener(new PropertyChangeListener() {
            public void propertyChange(PropertyChangeEvent e) {
                dcModifFinPropert(e);
            }
        });
    }

    public void dcModifInicioPropert(PropertyChangeEvent pe) {

        if (pe.getPropertyName().compareTo("date") == 0) {

            controlVista.validarFechaInicio(dcEditarInicio.getDate(), dcEditarFinal.getDate());

        }
    }

    public void dcModifFinPropert(PropertyChangeEvent pe) {
        if (pe.getPropertyName().compareTo("date") == 0) {

            controlVista.validarFechaFinM(dcEditarInicio.getDate(), dcEditarFinal.getDate());

        }
    }

