    @Override
    public void enviarFechaInicio(Date hoy, boolean valida) {
        if (!valida && hoy!=null) {
            dcCrearInicio.setDate(hoy);
        }
        if(!valida && hoy==null){
            ((JTextField) dcCrearInicio.getDateEditor().getUiComponent()).setText("");
            btCrearCiclo.setEnabled(false);
        }
        dcCrearFinal.setEnabled(true);
             
    }

    @Override
    public void enviarFechaFin(boolean valida) {
        System.out.println("entreeeee");
        if (!valida) {
            ((JTextField) dcCrearFinal.getDateEditor().getUiComponent()).setText("");

        }

        btCrearCiclo.setEnabled(valida);

    }

